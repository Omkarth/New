package edu.adelaide.sse_project.clientside;

public class ChefAddressData {

    final String id;
    final String data;

    public ChefAddressData(String id, String data) {
        this.id = id;
        this.data = data;
    }

    public String getId() {
        return id;
    }

    public String getData() {
        return data;
    }
}
